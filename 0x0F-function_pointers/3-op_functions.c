#include <stdio.h>
#include "3-calc.h"

/**
 * op_add - finds the sum of 2 integers
 * @a: 1st integer
 * @b: 2nd integer
 *
 * Return: sum of a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts 1 integer from another.
 * @a: 1st integer
 * @b: 2nd integer
 *
 * Return: difference of a and b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies a by b
 * @a: 1st integer
 * @b: 2nd integer
 *
 * Return: product of a and b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides 2 integers
 * @a: 1st integer
 * @b: 2nd integer
 *
 * Return: division of a by b
 */

int op_div(int a, int b);
{
	return (a / b);
}

/**
 * op_mod - finds the remainder
 * @a: 1st integer
 * @b: 2nd integer
 *
 * Return: remainder of the division of a by b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
